        <h1>INSTRUCTIONS AND REQUIREMENTS</h1>
        <h4>Application for any type of leave shall be made on this Form and to be accomplished at least in duplicate with documentaryrequirements, as follows:</h3>
        <div class="content"style="text-align: left;">
           <p ><strong>1. Vacation leave*</strong></p>
           <p>
            <li> It shall be filed five (5) days in advance, whenever possible, of the effective date of such leave. </li>
            <li> Vacation leave within in the Philippines or abroad shall be indicated in the form for purposes of securing travel authority and completing clearance from money and work accountabilities.</li>
           </p>

           <p ><strong>2. Mandatory/Forced leave</strong></p>
           <p>
            <li> Annual five-day vacation leave shall be forfeited if not taken during the year. </li>
            <li> In case the scheduled leave has been cancelled in the exigency of the service by the head of agency, it shall no longer be deducted from the accumulated vacation leave. </li>
            <li> Availment of one (1) day or more Vacation Leave (VL) shall be considered for complying the mandatory/forced leave subject to the conditions <span class="tooltip">Section 25, Rule XVI of the Omnibus Rules Implementing E.O. No. 292<span class="tooltip-text">This section mandates that employees must take a minimum of one (1) day vacation leave as part of the forced leave policy unless otherwise exempted.</span>
            </span>.
            </li>
           
           
           </p>
           <p ><strong>3. Sick leave*</strong></p>
           <p>
            <li> It shall be filed immediately upon employee's return from such leave.</li>
            <li> If filed in advance or exceeding five (5) days, application shall be accompanied by a medical certificate. </li>
            <li> In case medical consultation was not availed of, an affidavit should be executed by an applicant.</li>
           </p>
           <p ><strong>4.  Maternity leave*- 105 days</strong></p>
           <p>
            <li> Proof of pregnancy e.g. ultrasound, doctor's certificate on the expected date of delivery</li>
            <li> Accomplished Notice of Allocation of Maternity Leave Credits (CS Form No. 6a), if needed</li>
            <li> Seconded female employees shall enjoy maternityleave with full payin the recipient agency.</li>
           </p>
           <p ><strong>5.  Paternity leave - 7 days</strong></p>
           <p>
            <li>Proof of child’s delivery e.g. birth certificate, medical certificate and marriage contract.</li>
           </p>
           <p ><strong>6. Special Privilege leave - 3 days</strong></p>
           <p>
            <li>It shall be filed/approved for at least one (1) week prior to availment, except on emergency cases.</li>
            <li>Special privilege leave within the Philippines or abroad shall be indicated in the form for purposes of securing travel authority and completing clearance from money and workaccountabilities.</li>
           </p>
           <p ><strong>7. Solo Parent leave - 7 days</strong></p>
            <p>
                <li>It shall be filed in advance or whenever possible five (5) days before going on such leave with updated Solo Parent Identification Card.</li>
            </p>
           


           <p ><strong>8. Study leave* - up to 6 months</strong></p>
           <p>
           <li>Shall meet the agency’sinternal requirements, if any;</li>
           </p>
           
           <p>
           <li>Contract between the agency head or authorized representative and the employee concerned.</li>
           </p>


           <p ><strong>9. VAWC leave - 10 days</strong></p>
              <p>
                <li> It shall be filed in advanceor immediately upon the woman
                employee’s return from such leave. </li>
                </p>
                <p>
                <li>It shall be accompanied by any of the following supportingdocuments:</li>
                </p>
                <p>
                <li>a. Barangay Protection Order (BPO) obtained from the barangay;</li>
                </p>
                <p>
                <li>b. Temporary/Permanent Protection Order (TPO/PPO) obtained from the court;</li>
                </p>
                <p>
                <li>c. If the protection order is not yet issued by the barangay or the court, a certification issued by the Punong Barangay/Kagawad or Prosecutor or the Clerk of Court that the application for the BPO, TPO or PPO has been filed with the said office shall be sufficient to support the application for the ten-day leave; or</li>
                </p>
                <p>
                <li>d. In the absence of the BPO/TPO/PPO or the certification, a police report specifying the details of the occurrence of violence on the victim and a medical certificate may be considered, at the discretion of the immediate supervisor of the woman employee concerned. </li>
                </p>
              </p>

            <p><strong>10.Rehabilitation leave*-up to 6 months</strong></p>
            <p>
                <p>
                <li>Application shall be made within one (1)week from the time of the accident except when a longer period is warranted.</li>
                </p>
                <p>
                <li>Letter request supported by relevant reports suach as the police report, if any,</li>
                </p>
                <p>
                <li>Medical certificate on the nature of the injuries, the course of the treatment involved, the need to undergo rest, recuperation, and rehabilitation, as the case may be.</li>
                </p>
                <p>
                <li>Written concurrence of a government physician should be obtained relative to the recommendation for rehabilitation if the attending physician is a private pracitioner, particularly on the duration of the period of rehabilitation.</li>
                </p>
             </p>

                <p><strong>11.Special leave benefits for women*- up to 2 months</strong></p>
                <p>
                    <p>
                    <li>The application may be filed in advance, that is, at least five (5) days prior to the scheduled date of the  gynecological surgery that will be undergone by the employee. In case of emergency, the application for special leave shall be filed immediately upon employee's return but during confinement  the agency shall  be notified of said surgery. </li>
                    </p>
                    <p>
                    <li> The application shall be accompanied by a medical certificate filled out by the proper medical authorities, e.g. the attending surgeon accompanied by a clinical summary reflecting the gynecological disorder which shall be addressed or was addressed by the said surgery; the duration of the surgery including the peri- operative period (period of confinement around surgery); as well as the employeees estimated period for recuperation for the same.</li>
                    </p>

                    <p><strong>12. Special Emergency( Calamity) leave - up to 5 days</strong></p>
                    <p>
                         <li> The special emergency leave can  be applied for a maximum of five (5)  straight working days  or straggered basis within thirty (30) days from the actual occurence of the natural calamity/disaster. Said privilege shall be  enjoyed once a year, not in every instance of calamity or disaster.</li>
                   </p>
                   <p>
                    <li> The head of office shall take full responsibility for the grant of special emergency leave and verification of the employee's eligibility to be granted thereof. Said verification shall include: validation of place residence based on latest available  records of the affected employee; verification that the place of residence is covered in the declaration of calamity area by the proper government agency , and suach other proofs as may be necessary. </li>

                </p>
       
                </p>
                
            </p>


            <p><strong>13.Monetization of leave credits </strong></p>
            <p>
                <li>Application for monetization of fifty (50%) or more of the accumulated leave credits shall be accompanied by letter request to the head of the agency stating the valid and justifiable reasons.</li>

            </p>

            <p><strong>14.Terminal leave*</strong></p>
            <p>
                <li>Proof of employee's resignation or retirement or separation from the service.</li>
            </p>

            <p><strong>15.Adoption Leave</strong></p>
            <p>
                <li>Application for adoption leave shall be filed with an authenticated copy of the Pre-Adoptive Placement Authority issued by the Department of Social Welfare and Development (DSWD).</li>
            </p>

